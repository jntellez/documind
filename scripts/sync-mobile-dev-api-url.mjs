import fs from "node:fs/promises";
import os from "node:os";
import path from "node:path";
import { fileURLToPath } from "node:url";

const DEFAULT_API_PORT = "3000";
const GENERATED_HEADER = "# Auto-generated by Documind mobile dev workflow.";
const GENERATED_NOTE =
  "# Delete this file or set EXPO_PUBLIC_API_URL explicitly to override it.";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(scriptDir, "..");
const mobileDir = path.join(rootDir, "apps", "mobile");
const generatedEnvPath = path.join(mobileDir, ".env.development.local");
const envFileNames = [
  ".env.development.local",
  ".env.local",
  ".env.development",
  ".env",
];

function stripWrappingQuotes(value) {
  const trimmed = value.trim();

  if (
    (trimmed.startsWith('"') && trimmed.endsWith('"')) ||
    (trimmed.startsWith("'") && trimmed.endsWith("'"))
  ) {
    return trimmed.slice(1, -1).trim();
  }

  return trimmed;
}

function parseEnv(content) {
  const values = {};

  for (const rawLine of content.split(/\r?\n/)) {
    const line = rawLine.trim();

    if (!line || line.startsWith("#")) {
      continue;
    }

    const separatorIndex = line.indexOf("=");

    if (separatorIndex === -1) {
      continue;
    }

    const key = line.slice(0, separatorIndex).trim();
    const value = line.slice(separatorIndex + 1);

    values[key] = stripWrappingQuotes(value);
  }

  return values;
}

function isPrivateIpv4(address) {
  if (address.startsWith("10.")) {
    return true;
  }

  if (address.startsWith("192.168.")) {
    return true;
  }

  const [first, second] = address.split(".").map(Number);

  return first === 172 && second >= 16 && second <= 31;
}

function isIgnoredInterface(name) {
  return /^(lo|docker|veth|br-|virbr|vmnet|utun|tun|tap|tailscale|wg|zt|awdl)/i.test(
    name,
  );
}

function getInterfacePriority(name) {
  if (/^(en|eth)/i.test(name)) {
    return 4;
  }

  if (/^(wlan|wl|wi-?fi)/i.test(name)) {
    return 3;
  }

  return 1;
}

function selectLanIpv4() {
  const candidates = [];

  for (const [name, addresses] of Object.entries(os.networkInterfaces())) {
    if (!addresses || isIgnoredInterface(name)) {
      continue;
    }

    for (const details of addresses) {
      const family = typeof details.family === "string" ? details.family : String(details.family);

      if (family !== "IPv4" || details.internal) {
        continue;
      }

      const address = details.address?.trim();

      if (!address || address.startsWith("127.") || address.startsWith("169.254.")) {
        continue;
      }

      candidates.push({
        address,
        score: (isPrivateIpv4(address) ? 10 : 0) + getInterfacePriority(name),
      });
    }
  }

  candidates.sort((left, right) => right.score - left.score || left.address.localeCompare(right.address));

  return candidates[0]?.address ?? null;
}

async function readEnvFile(fileName) {
  const filePath = path.join(mobileDir, fileName);

  try {
    const content = await fs.readFile(filePath, "utf8");

    return {
      fileName,
      filePath,
      content,
      values: parseEnv(content),
      isGenerated: content.includes(GENERATED_HEADER),
    };
  } catch (error) {
    if (error && typeof error === "object" && "code" in error && error.code === "ENOENT") {
      return null;
    }

    throw error;
  }
}

async function removeGeneratedEnvIfPresent(envFiles) {
  const generatedFile = envFiles.find((file) => file?.fileName === ".env.development.local");

  if (!generatedFile?.isGenerated) {
    return;
  }

  await fs.unlink(generatedEnvPath);
}

async function main() {
  const envFiles = (await Promise.all(envFileNames.map(readEnvFile))).filter(Boolean);

  const explicitApiUrl = process.env.EXPO_PUBLIC_API_URL?.trim() || envFiles.find((file) => {
    const value = file.values.EXPO_PUBLIC_API_URL?.trim();

    if (!value) {
      return false;
    }

    return file.fileName !== ".env.development.local" || !file.isGenerated;
  })?.values.EXPO_PUBLIC_API_URL?.trim();

  if (explicitApiUrl) {
    await removeGeneratedEnvIfPresent(envFiles);
    console.log("[documind] Keeping explicit EXPO_PUBLIC_API_URL.");
    return;
  }

  const explicitApiPort =
    process.env.EXPO_PUBLIC_API_PORT?.trim() ||
    envFiles.find((file) => {
      const value = file.values.EXPO_PUBLIC_API_PORT?.trim();

      if (!value) {
        return false;
      }

      return file.fileName !== ".env.development.local" || !file.isGenerated;
    })?.values.EXPO_PUBLIC_API_PORT?.trim() ||
    DEFAULT_API_PORT;

  const lanIpv4 = selectLanIpv4();

  if (!lanIpv4) {
    await removeGeneratedEnvIfPresent(envFiles);
    console.warn(
      "[documind] Could not detect a LAN IPv4 address. Falling back to existing Expo/runtime config.",
    );
    return;
  }

  const apiUrl = `http://${lanIpv4}:${explicitApiPort}`;
  const content = `${GENERATED_HEADER}\n${GENERATED_NOTE}\nEXPO_PUBLIC_API_URL=${apiUrl}\n`;

  await fs.writeFile(generatedEnvPath, content, "utf8");
  console.log(`[documind] Synced mobile dev API URL to ${apiUrl}`);
}

main().catch((error) => {
  console.error("[documind] Failed to sync mobile dev API URL.");
  console.error(error);
  process.exitCode = 1;
});
